# ═══════════════════════════════════════════════════════════════
# Map locales to dictionaries
# ═══════════════════════════════════════════════════════════════
ALL_TRANSLATIONS = {
    'es': ES,
    'vi': VI,
    'th': TH,
    'id': ID,
    'ja': JA,
    'ko': KO,
}

# For locales without native translations, we skip (they already have EN)
LOCALES_TO_UPDATE = ['es', 'vi', 'th', 'id', 'ja', 'ko']


def fetch_rows_needing_update(locale):
    """Fetch vocabulary_ids that still have English fallback text for this locale."""
    url = (
        f"{SUPABASE_URL}/rest/v1/vocabulary_translations"
        f"?locale=eq.{locale}"
        f"&select=vocabulary_id,example_translation"
        f"&limit=500"
    )
    req = urllib.request.Request(url, headers=HEADERS)
    with urllib.request.urlopen(req) as resp:
        data = json.loads(resp.read().decode())

    en_patterns = [
        'I ', 'The ', 'My ', 'He ', 'She ', 'We ', 'They ',
        'This ', 'That ', 'Let', 'How ', 'What ', 'Where ',
        'Please ', 'Do ', 'Are ', 'Is ', 'It ', 'You ', 'A ', 'Have ', 'Did '
    ]

    needs_update = []
    for row in data:
        t = row.get('example_translation') or ''
        if any(t.startswith(w) for w in en_patterns):
            needs_update.append({
                'vocabulary_id': row['vocabulary_id'],
                'current_en_text': t,
            })

    return needs_update


def update_row(vocabulary_id, locale, new_translation):
    """PATCH a single row to update example_translation."""
    url = (
        f"{SUPABASE_URL}/rest/v1/vocabulary_translations"
        f"?vocabulary_id=eq.{vocabulary_id}&locale=eq.{locale}"
    )
    payload = json.dumps({"example_translation": new_translation}).encode()
    headers = {**HEADERS, "Prefer": "return=minimal"}
    req = urllib.request.Request(url, data=payload, headers=headers, method="PATCH")
    with urllib.request.urlopen(req) as resp:
        return resp.status


def main():
    total_updated = 0
    total_skipped = 0

    for locale in LOCALES_TO_UPDATE:
        trans_dict = ALL_TRANSLATIONS.get(locale, {})
        if not trans_dict:
            print(f"\n[{locale}] No translation dictionary, skipping")
            continue

        print(f"\n{'='*60}")
        print(f"Processing locale: {locale}")
        print(f"{'='*60}")

        rows = fetch_rows_needing_update(locale)
        print(f"  Found {len(rows)} rows needing update")

        updated = 0
        skipped = 0

        for row in rows:
            en_text = row['current_en_text']
            native_text = trans_dict.get(en_text)

            if not native_text:
                skipped += 1
                if skipped <= 3:
                    print(f"  [SKIP] No translation for: {en_text[:60]}")
                continue

            try:
                status = update_row(row['vocabulary_id'], locale, native_text)
                updated += 1
                if updated <= 3 or updated % 25 == 0:
                    print(f"  [{updated}] {en_text[:40]} -> {native_text[:40]}")
            except Exception as e:
                print(f"  [ERROR] {row['vocabulary_id']}: {e}")
                skipped += 1

        print(f"  Done: {updated} updated, {skipped} skipped")
        total_updated += updated
        total_skipped += skipped

    print(f"\n{'='*60}")
    print(f"TOTAL: {total_updated} rows updated, {total_skipped} skipped")
    print(f"{'='*60}")


if __name__ == "__main__":
    main()
