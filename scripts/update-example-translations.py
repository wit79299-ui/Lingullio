ALL_TRANSLATIONS = {
    'es': ES, 'vi': VI, 'th': TH, 'id': ID, 'ja': JA, 'ko': KO,
}

# ─── EXECUTION ─────────────────────────────────────────────────────────
# For each locale, update rows with example_pinyin (from EN) + example_translation

# First, get ALL vocabulary_translations rows for locales that need updating
LOCALES_TO_UPDATE = ['es', 'tr', 'de', 'it', 'pt', 'ja', 'ko', 'zh-Hans', 'zh-Hant',
                     'vi', 'th', 'id', 'hi', 'ar', 'ru', 'pl', 'ro', 'uk']

total_updated = 0

for locale in LOCALES_TO_UPDATE:
    print(f"\n{'='*50}")
    print(f"Processing {locale}...")

    # Get existing rows for this locale
    rows = supa_get('vocabulary_translations',
        f'select=id,vocabulary_id,example_sentence&locale=eq.{locale}&order=vocabulary_id&limit=500')
    print(f"  Found {len(rows)} existing rows")

    trans_dict = ALL_TRANSLATIONS.get(locale, {})
    updated = 0
    skipped = 0

    for row in rows:
        vid = row['vocabulary_id']
        pinyin = vid_pinyin.get(vid, '')
        en_trans = vid_en_trans.get(vid, '')

        if not pinyin and not en_trans:
            skipped += 1
            continue

        # Get translated example
        if locale in ALL_TRANSLATIONS and en_trans in ALL_TRANSLATIONS[locale]:
            translated = ALL_TRANSLATIONS[locale][en_trans]
        else:
            # Fallback: use EN translation if no locale-specific translation
            translated = en_trans

        # PATCH this row
        patch_data = {}
        if pinyin:
            patch_data['example_pinyin'] = pinyin
        if translated:
            patch_data['example_translation'] = translated

        if patch_data:
            status = supa_patch('vocabulary_translations',
                f'vocabulary_id=eq.{vid}&locale=eq.{locale}',
                patch_data)
            if status == 204:
                updated += 1
            else:
                print(f"    Failed for vid={vid}: status={status}")

    total_updated += updated
    matched = len(trans_dict)
    print(f"  Updated: {updated} rows, Native translations: {matched}, EN fallback: {updated - min(matched, updated)}")

print(f"\n{'='*60}")
print(f"Total rows updated: {total_updated}")
print(f"{'='*60}")
