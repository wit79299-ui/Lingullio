def supa_get(table, params=''):
    req = Request(f'{URL}/rest/v1/{table}?{params}', headers={'apikey': KEY, 'Authorization': f'Bearer {KEY}'})
    return json.loads(urlopen(req).read())

def supa_patch(table, filters, data):
    """PATCH (update) rows matching filters."""
    url = f'{URL}/rest/v1/{table}?{filters}'
    req = Request(url, data=json.dumps(data).encode('utf-8'), headers=HEADERS, method='PATCH')
    try:
        resp = urlopen(req)
        return resp.status
    except HTTPError as e:
        body = e.read().decode()[:200]
        print(f"    PATCH error {e.code}: {body}")
        return e.code

